public class CONDITIONAL_STATEMENT_SHORTCUT {

	public static void main(String[] args) {

		//CONDITIONS *STRING SHORTCUT*
		
		 int gradeOFSTUDENT1 = 91;
		 String result = (gradeOFSTUDENT1 < 100) ? "HIGHER" : "LOWER"; 
		 									//LEFT SIDE: "IF" RIGHT SIDE: "ELSE" *NO ELSE IF*
		 	System.out.println(result);  //kung saan nakatapat bunganga ng ">" && "<"  
		 								//yuon ang tama.

		/* PARTS
		 * (gradeOFSTUDENT1 < 100) ? "HIGHER" : "LOWER";  - THIS IS IF/ELSE STATEMENT
		 * 
		 *  "IF" STATEMENT "HIGHER" - RESULT IF ITS "TRUE"
		 *  "ELSE IF" STATEMENT "LOWER" - RESULT IF ITS "FALSE"
		 *  
		 *  ? = IF ITS TRUE
		 *  : = IF ITS FALSE
		 */

		 //CONDITIONS *INT/DOUBLE SHORTCUT*
		 	
		 int jon = 48;
		 int resultII = (jon == 60) ? 111 : 222;  // no need to put quotation marks in numbers.
		 		System.out.println(resultII);
		 		
	
		 
				 
		 
	}

}